/**
 * Build the project-level tools section of the install script.
 * @param {import('../data/tools-catalog.mjs').ToolDef[]} tools
 * @returns {string}
 */
export function renderToolsInstallSection(tools) {
  const ids = new Set(tools.map((t) => t.id));
  /** @type {string[]} */
  const lines = [];
  const vendored = tools.filter((t) => t.strategy === "vendor").map((t) => t.id);
  if (vendored.length > 0) {
    lines.push(`#   vendored into .claude/ (no machine install needed): ${vendored.join(", ")}`);
  }
  if (ids.has("rtk")) {
    lines.push(
      "#   rtk (token proxy) — PreToolUse hook already wired (guarded). Install the binary:",
    );
    lines.push(
      "#     brew install rtk   # or: curl -fsSL https://raw.githubusercontent.com/rtk-ai/rtk/refs/heads/master/install.sh | sh",
    );
  }
  if (ids.has("graphify")) {
    lines.push("#   graphify (code graph) — project-level:");
    lines.push("#     uv tool install graphifyy        # instala o binário graphify");
    lines.push(
      "#     graphify install --project       # registra a skill no .claude/skills/graphify/",
    );
    lines.push("#     graphify .                       # constrói o grafo inicial");
    lines.push("#     git add graphify-out/ && git commit -m 'chore: add graphify code graph'");
  }
  return lines.length > 0 ? lines.join("\n") : "#   (none)";
}

/**
 * A runnable cross-platform installer for the suggested plugins.
 * Idempotent; prompts unless run with `-y`.
 * Generates a .mjs Node.js script (not bash).
 * @param {import('../data/plugins-catalog.mjs').PluginSuggestion[]} plugins
 * @returns {string}
 */
export function renderPluginsInstallScript(plugins) {
  const names = [...new Set(plugins.map((p) => p.marketplace))];
  const mpList = JSON.stringify(
    names.map((n) => [n, marketplaceRepo(n)]),
    null,
    2,
  );
  const pluginList = JSON.stringify(
    plugins.map((p) => ({ name: p.name, marketplace: p.marketplace })),
    null,
    2,
  );
  const listing = plugins.map((p) => `  - ${p.name} [${p.purpose}] ${p.description}`).join("\n");

  return `#!/usr/bin/env node
// Generated by aia-harness — install the suggested Claude Code plugins.
// Plugins install at USER level (Claude Code has no per-project plugin install).
// Run with -y to skip the confirmation prompt.
import { spawnSync } from "node:child_process";
import * as rl from "node:readline/promises";

const check = spawnSync("claude", ["--version"], { stdio: "ignore" });
if (check.status !== 0) {
  process.stderr.write("aia-harness: 'claude' CLI not found — install Claude Code first.\\n");
  process.exit(1);
}

const marketplaces = ${mpList};
const plugins = ${pluginList};

if (!process.argv.includes("-y")) {
  const iface = rl.createInterface({ input: process.stdin, output: process.stdout });
  const ans = await iface.question(
    \`About to add \${marketplaces.length} marketplace(s) and install \${plugins.length} plugin(s):\n${listing}\nProceed? [y/N] \`
  );
  iface.close();
  if (!ans.match(/^y$/i)) { console.log("Aborted."); process.exit(0); }
}

for (const [, repo] of marketplaces) {
  spawnSync("claude", ["plugin", "marketplace", "add", repo], { stdio: "inherit" });
}
for (const p of plugins) {
  spawnSync("claude", ["plugin", "install", p.name + "@" + p.marketplace], { stdio: "inherit" });
}
console.log("Done. Restart Claude Code to load the new plugins.");
`;
}

