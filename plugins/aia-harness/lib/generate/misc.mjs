/**
 * @param {ProjectProfile} profile
 * @param {string[]} [gitignoreLines]  Lines from the project .gitignore (empty if none).
 * @returns {string}
 */
export function renderGraphifyignore(profile, gitignoreLines = []) {
  /** @type {string[]} */
  const sections = [
    "# .graphifyignore — gitignore syntax; seeded from project .gitignore + graphify extraction filters.",
  ];

  if (gitignoreLines.length > 0) {
    sections.push(...gitignoreLines, "");
  }

  const stackBase = [
    "node_modules/",
    "dist/",
    "build/",
    "out/",
    "coverage/",
    ".next/",
    ".nuxt/",
    ".turbo/",
    "*.min.js",
    "*.generated.*",
  ];
  /** @type {string[]} */
  const stackExtra = [];
  switch (profile.primaryLanguage) {
    case "Rust":
      stackExtra.push("target/");
      break;
    case "Java":
    case "Kotlin":
      stackExtra.push("target/", ".gradle/");
      break;
    case "PHP":
      stackExtra.push("vendor/");
      break;
    case "Go":
      stackExtra.push("vendor/", "bin/");
      break;
    case "Python":
      stackExtra.push("__pycache__/", ".venv/", "venv/", ".mypy_cache/");
      break;
    default:
      break;
  }
  sections.push(...stackBase, ...stackExtra, "", GRAPHIFY_MEDIA_BLOCK);

  return sections.join("\n") + "\n";
}

/**
 * Manual reference for project-level TOOLS + MCP servers. Plugins are NOT
 * duplicated here — they live in the runnable scripts/install-plugins.sh
 * (renderPluginsInstallScript). Everything below is commented: it includes
 * machine-level installs (rtk via brew, graphify via uv) that must be chosen,
 * so nothing auto-runs.
 * @param {string[]} mcpNames
 * @param {import('../data/tools-catalog.mjs').ToolDef[]} [tools]
 * @returns {string}
 */
export function renderInstallScript(mcpNames, tools = []) {
  const mcpComment =
    mcpNames.length > 0 ? mcpNames.map((n) => `#   - ${n}`).join("\n") : "#   (none recommended)";
  const toolLines = renderToolsInstallSection(tools);

  return `#!/usr/bin/env bash
# Generated by aia-harness — reference for project TOOLS + MCP servers.
# Review every line before running. Nothing here runs automatically.
# Plugins are installed separately — run scripts/install-plugins.sh (it is runnable),
# or let /aia-harness:init offer them interactively.
set -euo pipefail

# --- Project-level tools ----------------------------------------------------
${toolLines}

# --- MCP servers ------------------------------------------------------------
# The following strategic MCP servers were written to .mcp.json:
${mcpComment}
#
# Fill required secrets in .claude/settings.local.json (gitignored), then
# restart Claude Code so MCP servers load.

echo "Reference only. Plugins: run scripts/install-plugins.sh. Tools: uncomment a command above and re-run."
`;
}
