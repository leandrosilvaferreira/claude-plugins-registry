/** @typedef {import('../profile.mjs').ProjectProfile} ProjectProfile */

/** Map a primary language to a Language Server config. */
const LSP_SERVERS = {
  TypeScript: { command: "npx", args: ["-y", "typescript-language-server", "--stdio"] },
  JavaScript: { command: "npx", args: ["-y", "typescript-language-server", "--stdio"] },
  PHP: { command: "npx", args: ["-y", "intelephense", "--stdio"] },
  Python: { command: "npx", args: ["-y", "pyright-langserver", "--stdio"] },
  Go: { command: "gopls", args: [] },
  Rust: { command: "rust-analyzer", args: [] },
};

/**
 * @param {ProjectProfile} profile
 * @returns {string}
 */
export function renderStrategies(profile) {
  const c = profile.commands;
  const lang = profile.primaryLanguage ?? "unknown";
  return `# Harness strategies

Generated by aia-harness for a **${lang}** project (commands source: ${c.source}).

## Lint & format
${[c.lint && `- Lint: \`${c.lint}\``, c.format && `- Format: \`${c.format}\``].filter(Boolean).join("\n") || "- No linter detected. Consider adding one."}

## Compilation / typecheck
${c.typecheck ? `- \`${c.typecheck}\`` : "- No typecheck step detected."}

## Language server
${
  LSP_SERVERS[/** @type {keyof typeof LSP_SERVERS} */ (lang)]
    ? `- Suggested LSP: \`${LSP_SERVERS[/** @type {keyof typeof LSP_SERVERS} */ (lang)].command} ${LSP_SERVERS[/** @type {keyof typeof LSP_SERVERS} */ (lang)].args.join(" ")}\` (see \`.lsp.json\`).`
    : "- No standard language server mapping for this stack."
}

## Unit testing
${c.test ? `- \`${c.test}\`` : "- No test runner detected. Add one and wire it into CI."}

## Canonical command reference
${commandsBlock(c)}
`;
}

/**
 * @param {ProjectProfile} profile
 * @returns {string|null}
 */
export function renderLspJson(profile) {
  const lang = /** @type {keyof typeof LSP_SERVERS} */ (profile.primaryLanguage ?? "");
  const server = LSP_SERVERS[lang];
  if (!server) return null;
  const key = lang === "JavaScript" || lang === "TypeScript" ? "typescript" : lang.toLowerCase();
  return JSON.stringify({ lspServers: { [key]: { type: "stdio", ...server } } }, null, 2) + "\n";
}

